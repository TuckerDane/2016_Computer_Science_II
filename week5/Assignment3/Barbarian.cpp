#include "Barbarian.h"
#include "Die.h"
#include <string>

using std::cout;
using std::endl;
using std::cin;

/***************************************************************************************************
**	default constructor for Barbarian
***************************************************************************************************/
Barbarian::Barbarian()
{
	this->name = "Barbarian";
	this->armor = 0;
	this->strength = 12;
}

/***************************************************************************************************
**	constructor for Barbarian
***************************************************************************************************/
Barbarian::Barbarian(int a, int s)
{
	this->armor = a;
	this->strength = s;
}

/***************************************************************************************************
**	destructor for Barbarian
***************************************************************************************************/
Barbarian::~Barbarian()
{

}

/***************************************************************************************************
**	returns the amount of damage inflicted
***************************************************************************************************/
int Barbarian::attack()
{
	Die D6(6);
	return (D6.roll() + D6.roll());
}

/***************************************************************************************************
**	modifies attack damage given a Barbarian's defense roll
***************************************************************************************************/
int Barbarian::defend(int attack)
{
	cout << this->name << " defends against an attack of " << attack << endl;
	Die D6;
	return attack - (D6.roll() + D6.roll());
}

