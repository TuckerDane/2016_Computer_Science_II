/***************************************************************************************************
**	default constructor for Medusa
***************************************************************************************************/
Medusa::Medusa()
{
	this->name = "Medusa";
	this->armor = 3;
	this->strength = 8;
}

/***************************************************************************************************
**	constructor for Medusa
***************************************************************************************************/
Medusa::Medusa(int a, int s)
{
	this->armor = a;
	this->strength = s;
}

/***************************************************************************************************
**	destructor for Medusa
***************************************************************************************************/
Medusa::~Medusa()
{

}

/***************************************************************************************************
**	returns the amount of damage inflicted
***************************************************************************************************/
int Medusa::attack()
{
	Die D6(6);
	int rollValue = (D6.roll() + D6.roll());

	if (rollValue == 12)
	{
		cout << "GLARE SUCCEEDED" << endl;
		return 100;
	}
	else
	{
		return rollValue;
	}
}

/***************************************************************************************************
**	modifies attack damage given a Medusa's defense roll
***************************************************************************************************/
int Medusa::defend(int attack)
{
	cout << this->name << " defends against an attack of " << attack << endl;
	Die D6;

	return attack - D6.roll();
}
